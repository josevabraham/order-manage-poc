DROP TABLE IF EXISTS ADDRESS;
CREATE TABLE ADDRESS (
ADDRESS_ID bigint(20) NOT NULL AUTO_INCREMENT,
FIRST_NAME varchar(50) NOT NULL,
LAST_NAME varchar(50) NOT NULL,
STREET_ADDRESS_1 varchar(100) NOT NULL,
STREET_ADDRESS_2 varchar(100) ,
CITY varchar(50) NOT NULL,
STATE varchar(50) NOT NULL,
ZIP varchar(15) NOT NULL,
MODIFIED_DTM timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
CREATED_DTM timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
MODIFIER_ID bigint(20) NOT NULL,
PRIMARY KEY (ADDRESS_ID)
);

DROP TABLE IF EXISTS ONLINE_ORDER;
CREATE TABLE ONLINE_ORDER (
ONLINE_ORDER_ID bigint(20) NOT NULL AUTO_INCREMENT,
ORDER_NUMBER varchar(20) NOT NULL,
ORDER_PLACED_DATE timestamp NOT NULL DEFAULT '0000-00-00 00:00:00',
CUSTOMER_NAME varchar(100) NOT NULL,
SHIPPING_ADDRESS_ID bigint(20),
SELLING_PRICE double(10),
SALES_TAX double(10),
SHIPPING_PRICE double(10),
ORDER_STATUS varchar(100) NOT NULL,
MODIFIED_DTM timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
CREATED_DTM timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
MODIFIER_ID bigint(20) NOT NULL,
PRIMARY KEY (ONLINE_ORDER_ID),
CONSTRAINT R_SHIPPING_ADDRESS_ID FOREIGN KEY (SHIPPING_ADDRESS_ID) REFERENCES ADDRESS (ADDRESS_ID) ON DELETE NO ACTION ON UPDATE NO ACTION
);

DROP TABLE IF EXISTS ONLINE_ORDER_LINE;
CREATE TABLE ONLINE_ORDER_LINE (
ONLINE_ORDER_LINE_ID bigint(20) NOT NULL AUTO_INCREMENT,
ONLINE_ORDER_ID bigint(20) NOT NULL,
PRODUCT_ID bigint(20) NOT NULL,
PRODUCT_NAME varchar(100),
QUANTITY int(10),
LINE_STATUS varchar(100) NOT NULL,
MODIFIED_DTM timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
CREATED_DTM timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
MODIFIER_ID bigint(20) NOT NULL,
PRIMARY KEY (ONLINE_ORDER_LINE_ID),
CONSTRAINT R_ONLINE_ORDER_ID FOREIGN KEY (ONLINE_ORDER_ID) REFERENCES ONLINE_ORDER (ONLINE_ORDER_ID) ON DELETE NO ACTION ON UPDATE NO ACTION
);


